//箭头函数---------------------------------------------------
// 箭头函数和普通函数的区别：
// 1.没有自己的this,arguments,super
// 2.箭头函数是匿名函数,不能用new来调用
// 3.没有原型对象属性
// 4.内部的this无法改变,永远指向其上下文
// 5.形参名称不能重复

function A(a) {
    console.log(arguments);
}
let B = (...b) => {
    console.log(b);
}

console.log(B.prototype)  //undefined